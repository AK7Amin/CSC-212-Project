
public class visitorInfo {
	String fName, lName, phone;
	int region, type;
	ArrayStack <Integer> order;
	
	public visitorInfo(String fName, String iname, String phone, int region, int type, ArrayStack <Integer> order) {
		this.fName = fName;
		lName = iname;
		this.phone = phone;
		this.region = region;
		this.type = type;
		this.order = order;
	}
}
	 
	

